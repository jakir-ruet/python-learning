# binary literal

binLiteral = 10101011

# decimal literal
decLiteral = 150

# octal literal
octLiteral = 0o450

# hexadecimal literal
hexLiteral = 0x14c

print('binLiteral', binLiteral)
print('decLiteral', decLiteral)
print('octLiteral', octLiteral)
print('hexLiteral', hexLiteral)

# float literal
floatOne = 20.45
floatTwo = 2.5e5
print('floatOne', floatOne)
print('floatTwo', floatTwo)

# complex literal
cpxNumber = 3 + 3.5j

print('img number', cpxNumber)
print('real value', cpxNumber.real)
print('imaginary value', cpxNumber.imag)
