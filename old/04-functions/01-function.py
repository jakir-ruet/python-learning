# types
# 4 types function such as builtin, user-define, lamda amd recursive function.
# four part in a function such as function name, parameter list, body and return statement.
def mathOperation(a, b):
    # here a and b are parameters
    print("The summation = ", a + b)
    print("The subtraction = ", a - b)
    print("The multiplication = ", a * b)
    print("The division = ", a / b)


# the value 10, 5 are arguments
mathOperation(10, 5)

# From a function's perspective:
# A parameter is the variable listed inside the parentheses in the function definition.
# An argument is the value that are sent to the function when it is called.
